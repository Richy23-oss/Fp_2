
package eva2_18_while;


public class Eva2_18_WHILE {

   
    public static void main(String[] args) {
        int i =1;
        while(i <=10){ //se repite si la condicion es verdad
            System.out.println("Ricardo Lopez");
            i++;
    }
        //SIMULAR UN FOR CON DO WHILE
        i=1; 
        do{
            System.out.println("Ricardo Lopez");
            i++;
        }while(i<=10);
    }
}
