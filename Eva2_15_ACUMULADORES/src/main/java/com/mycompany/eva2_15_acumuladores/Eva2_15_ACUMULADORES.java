public class Eva2_15_ACUMULADORES {

    public static void main(String[] args) {
     int cuenta = 0;
     //ASIGNACION
     System.out.println(cuenta);
     cuenta=cuenta+100; //ACUMULADORES
     System.out.println(cuenta);
     cuenta=cuenta+1000;
     System.out.println(cuenta);
     cuenta=cuenta- 800; //CUENTA -= 800;
     cuenta+=2000;
     System.out.println(cuenta); //CUENTA = CUENTA + 2000
    }
}
